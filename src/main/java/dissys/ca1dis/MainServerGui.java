    private void ensureActivityStream() {
        if (activityReqObs != null) {
            return;
        }

        resultTextArea.setText("");
        resultTextArea.append("Activity tracking started...\n");

        activityReqObs = client.activityTrack(
                // server the last return ActivityReply（summary）
                reply -> javax.swing.SwingUtilities.invokeLater(() -> {
                    resultTextArea.append("\n=== Summary ===\n");
                    resultTextArea.append("Date: " + reply.getDay() + "\n");
                    resultTextArea.append("Total credit: " + reply.getPersonalCredit().getTotalCredit() + "\n");
                    resultTextArea.append("Total carbon: " + reply.getSummary().getCarbon() + "\n");
                }),
                err -> javax.swing.SwingUtilities.invokeLater(() -> {
                    resultTextArea.append("Error: " + err.getMessage() + "\n");
                    activityReqObs = null;
                }),
                () -> javax.swing.SwingUtilities.invokeLater(() -> {
                    resultTextArea.append("Activity stream completed.\n");
                })
        );
    }


