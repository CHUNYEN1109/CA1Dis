    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        // TODO add your handling code here:
        if (activityReqObs == null) {
            resultTextArea.append("No activity to finish.\n");
            return;
        }
        resultTextArea.append("DONE sent.\n");
        activityReqObs.onCompleted();
        activityReqObs = null;
    }//GEN-LAST:event_doneButtonActionPerformed

    private void productIdButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productIdButton1ActionPerformed
        // TODO add your handling code here:
        // input produc ID; return product detail
        String productId = productIdTextField.getText().trim();
        // if it is empty
        if (productId.isEmpty()) {
            resultTextArea.setText("Please, scan product ID. (e.g., A01)");
        }

        // set button
        productIdButton1.setEnabled(false);
        resultTextArea.setText("");
        resultTextArea.setText(productId + " is loadding...\n");
        // set thread
        new Thread(() -> {
            client.productTrack(
                    productId,
                    reply -> javax.swing.SwingUtilities.invokeLater(() -> {
                        resultTextArea.append(
                                "ProductId: " + reply.getProductId()
                                + "\nManufacturer: " + reply.getManufacturer()
                                + "\nDescription: " + reply.getDescription()
                                + "\nCarbon: " + reply.getCarbon()
                                + "\nImpactPoint: " + reply.getImpactPoint()
                                + "\n\n"
                        );
                    }),
                    err -> javax.swing.SwingUtilities.invokeLater(() -> {
                        resultTextArea.append("Error: " + err.getMessage() + "\n");
                        productIdButton1.setEnabled(true);
                    }),
                    () -> javax.swing.SwingUtilities.invokeLater(() -> {
                        resultTextArea.append("Completed\n");
                        productIdButton1.setEnabled(true);
                    })
            );
        }).start();


    }//GEN-LAST:event_productIdButton1ActionPerformed

